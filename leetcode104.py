# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution(object):
    def maxDepth(self, root):
        """
        :type root: TreeNode
        :rtype: int
        """
        if not root:
            return 0
        queue = [(1, root)]
        while queue:
            level, ptr = queue.pop(0)
            if ptr.left:
                queue.append((level + 1, ptr.left))
            if ptr.right:
                queue.append((level + 1, ptr.right))
        return level




